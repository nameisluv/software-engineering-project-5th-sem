    >Transmission of parasite. If this mosquito bites you in the future, it can transmit malaria parasites to you.<br>

    >In the liver. Once the parasites enter your body, they travel to your liver â€” where some types can lie dormant for as long as a year.<br>
    >Into the bloodstream. When the parasites mature, they leave the liver and infect your red blood cells. This is when people typically develop malaria symptoms.<br>
    >On to the next person. If an uninfected mosquito bites you at this point in the cycle, it will become infected with your malaria parasites and can spread them to the other people it bites.</p><br>

  <p><h4>Other modes of transmission</h4><br>
Because the parasites that cause malaria affect red blood cells, people can also catch malaria from exposure to infected blood, including:<br><br>

    >From mother to unborn child<br>
    >Through blood transfusions<br>
    >By sharing needles used to inject drugs<br>
