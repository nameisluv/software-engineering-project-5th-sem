Signs and symptoms of malaria may include:

            >Fever<br>
            >Chills<br>
            >General feeling of discomfort<br>
            >Headache<br>
            >Nausea and vomiting<br>
            >Diarrhea<br>
            >Abdominal pain<br>
            >Muscle or joint pain<br>
            >Fatigue<br>
            >Rapid breathing<br>
            >Rapid heart rate<br>
            >Cough<br>

Some people who have malaria experience cycles of malaria "attacks." An attack usually starts with shivering and chills, followed by a high fever, followed by sweating and a return to normal temperature.

Malaria signs and symptoms typically begin within a few weeks after being bitten by an infected mosquito. However, some types of malaria parasites can lie dormant in your body for up to a year.</p>
