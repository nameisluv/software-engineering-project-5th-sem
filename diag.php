<h4 id="diagnosis">Diagnosis</h4><p>To diagnose malaria, your doctor will likely review your medical history and recent travel, conduct a physical exam, and order blood tests. Blood tests can indicate:</p>
<ul>
    <li>The presence of the parasite in the blood, to confirm that you have malaria</li>
    <li>Which type of malaria parasite is causing your symptoms</li>
    <li>If your infection is caused by a parasite resistant to certain drugs</li>
    <li>Whether the disease is causing any serious complications</li>
</ul>
<p>Some blood tests can take several days to complete, while others can produce results in less than 15 minutes. Depending on your symptoms, your doctor may order additional diagnostic tests to assess possible complications.</p>
        
            
            
      <h4>Treatment</h4><p>Malaria is treated with prescription drugs to kill the parasite. The types of drugs and the length of treatment will vary, depending on:</p>
<ul>
    <li>Which type of malaria parasite you have</li>
    <li>The severity of your symptoms</li>
    <li>Your age</li>
    <li>Whether you're pregnant</li>
</ul>
        
            
            <h4>Medications</h4><p>The most common antimalarial drugs include:</p>
<ul>
    <li><strong>Chloroquine phosphate.</strong> Chloroquine is the preferred treatment for any parasite that is sensitive to the drug. But in many parts of the world, parasites are resistant to chloroquine, and the drug is no longer an effective treatment.</li>
    <li><strong>Artemisinin-based combination therapies (ACTs).</strong> <abbr title="Artemisinin-based combination therapy">ACT</abbr> is a combination of two or more drugs that work against the malaria parasite in different ways. This is usually the preferred treatment for chloroquine-resistant malaria. Examples include artemether-lumefantrine (Coartem) and artesunate-mefloquine.</li>
</ul>
<p>Other common antimalarial drugs include:</p>
<ul>
    <li>Atovaquone-proguanil (Malarone)</li>
    <li>Quinine sulfate (Qualaquin) with doxycycline (Oracea, Vibramycin, others)</li>
    <li>Primaquine phosphate</li>
</ul>
        
            <h4>Preparing for your appointment</h4><p>If you suspect you have malaria or that you've been exposed, you're likely to start by seeing your family doctor. However, in some cases when you call to set up an appointment, you may be referred to an infectious disease specialist. If you have severe symptoms — especially during or after travel in an area where malaria is common — seek emergency medical attention.</p>
