<p><h4>Risk factors</h4><br>

The greatest risk factor for developing malaria is to live in or to visit areas where the disease is common. These include the tropical and subtropical regions of:<br><br>

    >Sub-Saharan Africa<br>
    >South and Southeast Asia<br>
    >Pacific Islands<br>
    >Central America and northern South America<br>

The degree of risk depends on local malaria control, seasonal changes in malaria rates and the precautions you take to prevent mosquito bites.
</p><br>
<p>
<h4>Risks of more-severe disease</h4><br>

People at increased risk of serious disease include:
<br><br>
>Young children and infants<br>
>Older adults<br>
>Travelers coming from areas with no malaria<br>
>Pregnant women and their unborn children<br>

In many countries with high malaria rates, the problem is worsened by lack of access to preventive measures, medical care and information.
</p><br>
<p><h4>Immunity can wane</h4><br>

Residents of a malaria region may be exposed to the disease enough to acquire a partial immunity, which can lessen the severity of malaria symptoms. However, this partial immunity can disappear if you move to a place where you're no longer frequently exposed to the parasite.</p><br>
<p><h4>Complications</h4><br>

Malaria can be fatal, particularly when caused by the plasmodium species common in Africa. The World Health Organization estimates that about 94% of all malaria deaths occur in Africa — most commonly in children under the age of 5.<br><br>

Malaria deaths are usually related to one or more serious complications, including:<br><br>

    >Cerebral malaria. If parasite-filled blood cells block small blood vessels to your brain (cerebral malaria), swelling of your brain or brain damage may occur. Cerebral malaria may cause seizures and coma.<br>
    >Breathing problems. Accumulated fluid in your lungs (pulmonary edema) can make it difficult to breathe.<br>
    >Organ failure. Malaria can damage the kidneys or liver or cause the spleen to rupture. Any of these conditions can be life-threatening.<br>
    >Anemia. Malaria may result in not having enough red blood cells for an adequate supply of oxygen to your body's tissues (anemia).<br>
    >Low blood sugar. Severe forms of malaria can cause low blood sugar (hypoglycemia), as can quinine — a common medication used to combat malaria. Very low blood sugar can result in coma or death.
</p><br>
<p><h4>Malaria may recur</h4><br>

Some varieties of the malaria parasite, which typically cause milder forms of the disease, can persist for years and cause relapses.
Prevention<br><br>

If you live in or are traveling to an area where malaria is common, take steps to avoid mosquito bites. Mosquitoes are most active between dusk and dawn. To protect yourself from mosquito bites, you should:<br><br>

    Cover your skin. Wear pants and long-sleeved shirts. Tuck in your shirt, and tuck pant legs into socks.
    Apply insect repellent to skin. Use an insect repellent registered with the Environmental Protection Agency on any exposed skin. These include repellents that contain DEET, picaridin, IR3535, oil of lemon eucalyptus (OLE), para-menthane-3,8-diol (PMD) or 2-undecanone. Do not use a spray directly on your face. Do not use products with OLE or PMD on children under age 3.
    Apply repellent to clothing. Sprays containing permethrin are safe to apply to clothing.
    Sleep under a net. Bed nets, particularly those treated with insecticides, such as permethrin, help prevent mosquito bites while you are sleeping.
</p><br>
<p><h4>Preventive medicine</h4><br>

If you'll be traveling to a location where malaria is common, talk to your doctor a few months ahead of time about whether you should take drugs before, during and after your trip to help protect you from malaria parasites.<br><br>

In general, the drugs taken to prevent malaria are the same drugs used to treat the disease. What drug you take depends on where and how long you are traveling and your own health.
Vaccine<br><br>

The World Health Organization has recommended a malaria vaccine for use in children who live in countries with high numbers of malaria cases.<br><br>

Researchers are continuing to develop and study malaria vaccines to prevent infection.</p><br><br>




